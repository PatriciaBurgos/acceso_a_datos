    private void jButton4_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4_guardarActionPerformed
        Animal nuevo_a = new Animal();
        nuevo_a.setNombre(this.jTextField1_nombre.getText());
        nuevo_a.setProvincia(this.jTextField3_provincia.getText());
        nuevo_a.setAnios(Integer.valueOf(this.jTextField2_edad.getText()));
        if(this.jTextField4_familia.getText()== "si" && this.jTextField4_familia.getText()== "Si")
            nuevo_a.setFamilia(true);
        else
            nuevo_a.setFamilia(false);
        
        animales.add(nuevo_a);
        modelo.addRow(new Object[]{animales.get(this.animales.size()-1).getNombre(),animales.get(this.animales.size()-1).getProvincia()});
        
        this.vaciar_campos_detallados();
        this.vaciar_animal_modificar();
        this.editable_no();        
        this.modif=false;
        this.desactivar_botones_gc();
        this.activar_botones_amb();
    }//GEN-LAST:event_jButton4_guardarActionPerformed

    private void jButton2_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2_modificarActionPerformed
        int row = 0;
        row = this.jTable1_tabla.getSelectedRow();
                
        this.modif=true;
        
        this.editable_si();
        this.rellenar_campos_detallados();
        this.rellenar_animal_modificar();
                
        modelo.removeRow(row);
        this.animales.remove(row);
        modelo.fireTableDataChanged();
        this.activar_botones_gc();
        this.desactivar_botones_amb();
    }//GEN-LAST:event_jButton2_modificarActionPerformed

