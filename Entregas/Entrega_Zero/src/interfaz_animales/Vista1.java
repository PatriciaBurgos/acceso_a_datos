    private void jButton5_cancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5_cancelarActionPerformed
        if(modif==true){
            Animal nuevo = new Animal();
            nuevo.setAnios(animal_modificar.getAnios());
            nuevo.setNombre(animal_modificar.getNombre());
            nuevo.setProvincia(animal_modificar.getProvincia());
            nuevo.setFamilia(animal_modificar.isFamilia());
            
            animales.add(nuevo);
            modelo.addRow(new Object[]{animales.get(this.animales.size()-1).getNombre(),animales.get(this.animales.size()-1).getProvincia()});
        
            this.vaciar_animal_modificar();         
            modelo.fireTableDataChanged();
        }
        this.modif=false;
        this.vaciar_campos_detallados();
        this.editable_no();
        this.desactivar_botones_gc();
        this.activar_botones_amb();
    }//GEN-LAST:event_jButton5_cancelarActionPerformed

